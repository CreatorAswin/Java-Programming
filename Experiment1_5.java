public class Experiment1_5 {
        public static void main(String[] args) {
            // Using a for loop to print numbers from 1 to 5
            System.out.println("Using a for loop:");
            for (int i = 1; i <= 5; i++) {
                System.out.println(i);
            }
    
            // Using a while loop to print numbers from 6 to 10
            System.out.println("\nUsing a while loop: ");
            int j = 6;
            while (j <= 10) {
                System.out.println(j);
                j++;
            }
    
            // Using a do-while loop to print numbers from 11 to 15
            System.out.println("\nUsing a do-while loop:");
            int k = 11;
            do {
                System.out.println(k);
                k++;
            } while (k <= 15);
    }
}
    
