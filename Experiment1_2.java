public class Experiment1_2 {
        public static void main(String[] args) {
            // Demonstrating different data types
    
            // String: For text
            String name = "M.Aswini Kumar";
            String regNo = "EMPVIZ0198";
            String branch = "Computer Science";
            String domain = "Software Technology";
    
            // Integer: For whole numbers
            int marks = 97;
    
            // Double: For precise decimal values
            double cgpa = 9.5;
    
            // Boolean: For true/false values
            boolean isPassed = marks >= 40;
    
            // Character: For single characters
            char grade = 'A';
    
            // Displaying the information
            System.out.println("Student Details: ");
            System.out.println("Name: " + name);
            System.out.println("Registration Number: " + regNo);
            System.out.println("Branch: " + branch);
            System.out.println("Domain: " + domain);
            System.out.println("Marks: " + marks);
            System.out.println("CGPA: " + cgpa);
            System.out.println("Grade: " + grade);
            System.out.println("Passed: " + isPassed);
        }
    
    
}
